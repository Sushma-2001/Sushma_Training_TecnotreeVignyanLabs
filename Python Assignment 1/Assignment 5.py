#Enter the input from the user
n1 = int(input("Enter the first number: "))
n2 = int(input("Enter the second number: "))

#Perform arithmetic operations
addition = n1 + n2
subtraction = n1 - n2
multiplication = n1 * n2
division = n1 / n2

#print the result 
print(f"The result of adding {n1} and {n2} is {addition}")
print(f"The result of subtracting {n2} from {n1} is {subtraction}")
print(f"The result of multiplying {n1} and {n2} is {multiplication}")
print(f"The result of dividing {n1} by {n2} is {division}")
